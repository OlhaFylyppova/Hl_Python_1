# Програма має перенести останній елемент списку з кінця на початок, тобто, останній
# елемент списку має стати першим. Послідовність інших елементів не має змінюватися.
# Порожній список або список з одним елементом повинен залишитися незмінним.

my_list = [12, 3, 4, 10, 8]
print(f"start list: {my_list}")
if len(my_list) > 1:
    my_list.insert(0, my_list[-1])
    my_list.pop()
print(f"result list: {my_list}")
